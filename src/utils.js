const shuffleArray = (array) => {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const temp = array[i];
    array[i] = array[j];
    array[j] = temp;
  }
};

const displayPercent = (percent) => `${(percent * 100).toFixed(0)}%`;

export { shuffleArray, displayPercent };
